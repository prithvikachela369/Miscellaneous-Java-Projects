
public class AdvancedMathFunctionsInJava {

	public static void main(String[] args) {
		System.out.println(Math.class);
		
		System.out.println(Math.E);//Base of natural Logarithm
		
		System.out.println(Math.PI);
		
		System.out.println(Math.sqrt(100));//SquareRoot
		
		System.out.println(Math.abs(-19));
		
		System.out.println(Math.acos(.78));
		
		System.out.println(Math.addExact(6, 7));//Adds The Exact Numbers
		
		System.out.println(Math.asin(.90));
		
		System.out.println(Math.atan(-.1));
		
		System.out.println(Math.atan2(10, 8));
		
		System.out.println(Math.cbrt(27));//Cube Root
		
		System.out.println(Math.ceil(10.1));//It Rounds Up The Value
		
		System.out.println(Math.copySign(-40, 10));//It Takes sign of second number and puts in first one
		
		System.out.println(Math.cos(35.7));
		
		System.out.println(Math.cosh(9));
		
		System.out.println(Math.decrementExact(3));//Decrements Number By One
		
		System.out.println(Math.exp(2));//Here we raise the power of E( see 7th Line), here we raised it to power 2
		
		System.out.println(Math.expm1(1));
		
		System.out.println(Math.floor(10.3));//It Rounds Down The Value
		
		System.out.println(Math.floorDiv(75, 2));//It divides The dividend(75) to the divisor(2) and then floor it
		
		System.out.println(Math.floorMod(2, 3));//Returns the floor modulus of the int arguments.
		
		System.out.println(Math.getExponent(193));//Gives the unbiased exponent
		
		System.out.println(Math.fma(2, 2, 2));
		
		System.out.println(Math.hypot(3, 4));//Returns Hypotenuse(Pythagoraes theorem)
		
		System.out.println(Math.IEEEremainder(1, 2));
		
		System.out.println(Math.incrementExact(120));
		
		System.out.println(Math.log(Math.E));
		
		System.out.println(Math.log10(29));
		
		System.out.println(Math.log1p(10));
		
		System.out.println(Math.max(193, 223));//Returns the bigger number
		
		System.out.println(Math.min(10, 78));//Returns the smaller number
		
		System.out.println(Math.multiplyExact(12, 8));
		
		System.out.println(Math.negateExact(12));
		
		System.out.println(Math.nextAfter(2, 1));
		
		System.out.println(Math.nextDown(10));
		
		System.out.println(Math.nextUp(20));
		
		System.out.println(Math.pow(3, 2));//It is 3^2(a^b)
		
		System.out.println(Math.random());//Here Without any parameters it generates number between 0 and 1
		
		System.out.println(Math.rint(3.9));
		
		System.out.println(Math.round(23.50001));
		
		System.out.println(Math.scalb(2, 3));
		
		System.out.println(Math.signum(34.76));
		
		System.out.println(Math.sin(23));
		
		System.out.println(Math.sinh(-12));
		
		System.out.println(Math.subtractExact(98, 123));
		
		System.out.println(Math.tan(-.97));
		
		System.out.println(Math.tanh(-9));
		
		System.out.println(Math.toDegrees(12));//Converts to degrees
		
		System.out.println(Math.toIntExact((long) 378.978));
		
		System.out.println(Math.toRadians(45));//Converts to radians
		
		System.out.println(Math.ulp(28.9890));
	}

}
